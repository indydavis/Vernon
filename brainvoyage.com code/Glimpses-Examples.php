<H1 CLASS="title">Examples, Glimpses & Glossary - Reality Begins With Consciousness</H1>
<br /><br />
<h2>The hidden reality</h2>
We present schemata. 
But we present only what we can see.
<em>These diagrams are metaphorical to conceptualize complex concepts.</em>
The picture below may appear 3 dimensional because we cannot portray information in 9 dimensions -- our reality may look like this but in actuality most of reality is hidden but we cannot see them just like we cannot see the extra dimensions in this schematic diagram.


<br><br>
<center><img src="Glimpses-Examples-1.png"></center>
<br><br>
"Glimpses" portray schematic representations only.
This may assist conceptualization but within limitations. 
That's why there is a book.
And that's why we have some text here.
This portrait purely introduces the complexity of the diagram. 
Do not try to interpret these diagrams beyond that. 
The "glimpses" of specifics begin on the next page.
Please recognize its limitations and strengths.
They are meant as aids to the book 
Reality Begins with Consciousness and 
to our TDVP model
--The Triadic Dimensional-Distinction Vortical Paradigm 


<h2 style="margin-top: 40px;">The triad: STC Unification in the TDVP paradigm:</h2>
Space (S), Time (T) and Consciousness (C) (STC) substrates 
<em>STC all exist but they are separate entities.
STC makes up the TRIAD in TDVP
Yet STC is always inseparably "tethered" together.</em>
You cannot ever completely separate them: 
They're linked no matter how much you think they are not.
You can never have space without time and without consciousness; nor time without space and consciousness; nor consciousness without time and space. They cannot exist separately.
But we experience different degrees of each depending on which "domain" of reality we perceive.
<br><br>
<center><img src="Glimpses-Examples-2.png"></center>
<br><br>
Schematically, Space (S),Time (T) and "Consciousness" (C) appear here to be the same size but they may vary profoundly in size.
This is because each of S, T and C reflect different dimensions.
All dimensions have size: 
we can measure the size by complex mathematics.
It requires some consciousness to make that interpretation: 
STC is relative to the domains we're experiencing them in.
There is an objective reality but we can never get there.
We can only know our own finite subjective state.
